// lookupChinaCity 按城市名查坐标，返回 (lat, lon, found)。
// 尝试精确匹配，再去"市/区/县"后缀匹配。
func lookupChinaCity(name string) (lat, lon float64, ok bool) {
	if name == "" {
		return 0, 0, false
	}
	if c, found := chinaCityCoords[name]; found {
		return c[0], c[1], true
	}
	// 去掉常见后缀
	for _, suf := range []string{"市", "区", "县", "自治州", "地区"} {
		if len(name) > len(suf) {
			trimmed := name[:len(name)-len(suf)]
			if c, found := chinaCityCoords[trimmed]; found {
				return c[0], c[1], true
			}
		}
	}
	return 0, 0, false
}
